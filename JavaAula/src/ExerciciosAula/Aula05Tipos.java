package ExerciciosAula;

public class Aula05Tipos {

	public static void main(String[] args) {
		
		float preco = 10.99f;     
		//float preco2 = (float) 10.99;
		
		System.out.println("Valor do Produto: " + preco);
		
		char letra = 'S';                           //Sim ou Não
		System.out.println(letra);
		
		boolean ligado = true;          // Ligado é verdadeiro (True= Verdade)
		System.out.println(ligado);
		
		boolean desligado = false;
		System.out.println(desligado);
		
		double precoDouble = 10.99;             //Não precisa por o D no final
		System.out.println(precoDouble);
		
	
		
		

	}

}
